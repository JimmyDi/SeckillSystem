package com.di.entity;
public class Book{
	private long bookId;
	private String name;
	private int number;
	
	public void setBoodId(long bookId){
		this.bookId=bookId;
	}
	public long getBookId(){
		return this.bookId;
	}
	public void setName(String name){
		this.name=name;
	}
	public String getName(){
		return this.name;
	}
	public void setNumber(int number){
		this.number=number;
	}
	public int getNumber(){
		return this.number;
	}
	
	@Override
	public String toString(){
		return "bookId: " + bookId + "name: " + name + "number: "+number;
	}
}